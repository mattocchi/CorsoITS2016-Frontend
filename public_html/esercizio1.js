'use strict';
/* 
 * concatenazione di stringhe
 * 
 * concatenare con operatore +
 * due variabili di tipo diverso
 * la variabile "numero" e la variabile "testo"
 * 
 */
var numero = 1;
var testo = " parola";

console.log(numero + testo);

console.log(2 + 3 + testo);
console.log(testo + 2 + 3);
console.log(testo + (2 + 3));

console.log(2 + "3" + testo);
